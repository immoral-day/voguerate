class CurateDemoContent extends Command
{
    protected $signature = 'app:curate-demo-content
        {--apply : Apply the prepared content changes}';

    protected $description = 'Replace known demo and test text with coherent presentation content';

    public function handle(): int
    {
        $apply = (bool) $this->option('apply');
        $changes = [];

        $operation = function () use ($apply, &$changes): void {
            $this->curateUsers($apply, $changes);
            $this->curateReviews($apply, $changes);
            $this->curateChat($apply, $changes);
            $this->curateAuthorshipRequests($apply, $changes);
            $this->curateCatalog($apply, $changes);
            $this->curateEditorialContent($apply, $changes);
            $this->curateModerationAndFeedback($apply, $changes);
        };

        if ($apply) {
            DB::transaction($operation);
        } else {
            $operation();
        }

        $rows = collect($changes)
            ->sortKeys()
            ->map(fn (array $counts, string $section): array => [
                $section,
                $counts['updated'] ?? 0,
                $counts['deleted'] ?? 0,
            ])
            ->values()
            ->all();

        $this->table(['Раздел', 'Обновить', 'Удалить'], $rows);

        if ($apply) {
            $this->info('Смысловая очистка демонстрационных данных завершена.');
        } else {
            $this->warn('Это предпросмотр. Для применения запустите команду с --apply.');
        }

        return self::SUCCESS;
    }


    private function updateById(
        string $table,
        int $id,
        array $values,
        bool $apply,
        array &$changes,
        string $section,
    ): void {
        if (!Schema::hasTable($table)) {
            return;
        }

        $values = array_filter(
            $values,
            fn (string $column): bool => Schema::hasColumn($table, $column),
            ARRAY_FILTER_USE_KEY,
        );

        if ($values === []) {
            return;
        }

        $current = DB::table($table)->where('id', $id)->first(array_keys($values));
        if (!$current) {
            return;
        }

        $prepared = [];
        foreach ($values as $column => $value) {
            $storedValue = is_array($value)
                ? json_encode($value, JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES)
                : $value;

            if ((string) ($current->{$column} ?? '') !== (string) $storedValue) {
                $prepared[$column] = $storedValue;
            }
        }

        if ($prepared === []) {
            return;
        }

        $changes[$section]['updated'] = ($changes[$section]['updated'] ?? 0) + 1;
        if ($apply) {
            if (Schema::hasColumn($table, 'updated_at')) {
                $prepared['updated_at'] = now();
            }
            DB::table($table)->where('id', $id)->update($prepared);
        }
    }

    private function deleteKnownIds(
        string $table,
        array $ids,
        bool $apply,
        array &$changes,
        string $section,
    ): void {
        if (!Schema::hasTable($table) || $ids === []) {
            return;
        }

        $count = DB::table($table)->whereIn('id', $ids)->count();
        if ($count === 0) {
            return;
        }

        $changes[$section]['deleted'] = ($changes[$section]['deleted'] ?? 0) + $count;
        if ($apply) {
            DB::table($table)->whereIn('id', $ids)->delete();
        }
    }
}
